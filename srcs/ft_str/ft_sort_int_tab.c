#include "libft.h"

void	ft_sort_int_tab(int *tab, int size)
{
	int	a;
	int	b;
	int	c;

	a = 0;
	while (a < size)
	{
		b = 1;
		while (b < size)
		{
			if (tab[a] > tab[b] && a < b)
			{
				c = tab[a];
				tab[a] = tab[b];
				tab[b] = c;
			}
			b++;
		}
		a++;
	}
}
